package org.saved4.saved4.enums;

/**
 * Enum representing the different types of notifications that can be generated
 * in the system. These types categorize notifications for easier filtering and
 * processing in both frontend and backend operations.
 */
public enum NotificationType {
    ORDER_CREATION,
    ORDER_UPDATE,
    ORDER_DELETION,
    MESSAGE,   
    MISCELLANEOUS;
}